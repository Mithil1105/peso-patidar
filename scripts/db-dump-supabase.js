/**
 * Backup Supabase database by trying multiple connection formats.
 * Usage: node scripts/db-dump-supabase.js
 * Uses: SUPABASE_DB_PASSWORD and optionally SUPABASE_PROJECT_REF from .env or env.
 * Default project ref: yttrgclcrfrvlvtpfiso (or set in .env as SUPABASE_PROJECT_REF)
 */

import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import pg from "pg";

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, "..");
const envPath = join(rootDir, ".env");
const outPath = join(rootDir, "backup.sql");

function loadEnv() {
  try {
    const content = readFileSync(envPath, "utf8");
    const env = {};
    for (const line of content.split("\n")) {
      const m = line.match(/^\s*([^#][^=]+)=(.*)$/);
      if (m) env[m[1].trim()] = m[2].trim().replace(/^["']|["']$/g, "");
    }
    return env;
  } catch (e) {
    return {};
  }
}

const env = loadEnv();
const password = env.SUPABASE_DB_PASSWORD || process.env.SUPABASE_DB_PASSWORD || "ChaosInTheWake11";
const projectRef = env.SUPABASE_PROJECT_REF || process.env.SUPABASE_PROJECT_REF || "yttrgclcrfrvlvtpfiso";

// If DATABASE_URL is set and valid (contains @), try it first and use only that
const envDbUrl = env.DATABASE_URL || process.env.DATABASE_URL;
const useEnvUrlOnly = envDbUrl && envDbUrl.includes("@");

const enc = (p) => encodeURIComponent(p);
const urls = useEnvUrlOnly
  ? [envDbUrl]
  : [
      `postgresql://postgres:${enc(password)}@db.${projectRef}.supabase.co:5432/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-ap-south-1.pooler.supabase.com:5432/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-ap-south-1.pooler.supabase.com:6543/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-us-east-1.pooler.supabase.com:5432/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-us-east-1.pooler.supabase.com:6543/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-eu-west-1.pooler.supabase.com:5432/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-eu-west-1.pooler.supabase.com:6543/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-ap-southeast-1.pooler.supabase.com:6543/postgres`,
      `postgresql://postgres.${projectRef}:${enc(password)}@aws-0-ca-central-1.pooler.supabase.com:6543/postgres`,
    ];

function escapeSql(val) {
  if (val === null) return "NULL";
  if (typeof val === "boolean") return val ? "true" : "false";
  if (typeof val === "number" && !Number.isNaN(val)) return String(val);
  if (Buffer.isBuffer(val)) return "'" + val.toString("hex").replace(/'/g, "''") + "'";
  return "'" + String(val).replace(/'/g, "''").replace(/\\/g, "\\\\") + "'";
}

async function tryConnect(url, logError) {
  const client = new pg.Client({ connectionString: url, connectionTimeoutMillis: 10000 });
  try {
    await client.connect();
    return client;
  } catch (e) {
    if (logError) console.log(" ", e.message || e.code);
    try { await client.end(); } catch (_) {}
    return null;
  }
}

async function dumpWithClient(client) {
  const lines = [];
  lines.push("-- Backup generated by db-dump-supabase.js");
  lines.push("-- " + new Date().toISOString());
  lines.push("");

  const tablesRes = await client.query(`
    SELECT table_schema, table_name
    FROM information_schema.tables
    WHERE table_schema = 'public' AND table_type = 'BASE TABLE'
    ORDER BY table_schema, table_name
  `);

  for (const { table_schema, table_name } of tablesRes.rows) {
    const fullName = `"${table_schema}"."${table_name}"`;
    lines.push(`-- Table: ${fullName}`);
    const colsRes = await client.query(
      `SELECT column_name, data_type, is_nullable, column_default
       FROM information_schema.columns
       WHERE table_schema = $1 AND table_name = $2
       ORDER BY ordinal_position`,
      [table_schema, table_name]
    );
    const colNames = colsRes.rows.map((r) => r.column_name);
    const colList = colNames.map((c) => `"${c}"`).join(", ");
    const createParts = colsRes.rows.map((c) => {
      let def = `"${c.column_name}" ${c.data_type}`;
      if (c.column_default) def += " DEFAULT " + c.column_default;
      if (c.is_nullable === "NO") def += " NOT NULL";
      return def;
    });
    lines.push(`CREATE TABLE IF NOT EXISTS ${fullName} (`);
    lines.push(createParts.join(",\n"));
    lines.push(");");
    lines.push("");

    const res = await client.query(`SELECT * FROM ${fullName}`);
    for (const row of res.rows) {
      const vals = colNames.map((c) => escapeSql(row[c]));
      lines.push(`INSERT INTO ${fullName} (${colList}) VALUES (${vals.join(", ")});`);
    }
    if (res.rows.length > 0) lines.push("");
  }

  writeFileSync(outPath, lines.join("\n"), "utf8");
}

async function main() {
  if (useEnvUrlOnly) console.log("Using DATABASE_URL from .env...");
  else console.log("Trying to connect to Supabase (project ref:", projectRef, ")...");
  let client = null;
  let usedUrl = null;
  for (let i = 0; i < urls.length; i++) {
    const url = urls[i];
    const host = url.match(/@([^/]+)/)?.[1] || "?";
    process.stdout.write("  Try " + (i + 1) + "/" + urls.length + " (" + host + ")... ");
    client = await tryConnect(url, i === 0);
    if (client) {
      console.log("OK");
      usedUrl = url;
      break;
    }
    console.log("failed");
  }

  if (!client) {
    console.error("\nCould not connect with any Supabase host. Check:");
    console.error("  1. Project ref and password in .env (SUPABASE_PROJECT_REF, SUPABASE_DB_PASSWORD)");
    console.error("  2. Project is not paused (Supabase Dashboard)");
    console.error("  3. Copy the exact Connection string (URI) from Dashboard -> Project Settings -> Database");
    process.exit(1);
  }

  try {
    console.log("Connected. Dumping schema and data...");
    await dumpWithClient(client);
    console.log("Backup saved to backup.sql");
  } finally {
    await client.end();
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
