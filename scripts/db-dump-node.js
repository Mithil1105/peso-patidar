/**
 * Database backup without Docker or pg_dump.
 * Reads DATABASE_URL from .env in project root and writes backup.sql.
 * Requires: npm install (pg is a devDependency)
 */

import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";
import pg from "pg";

const __dirname = dirname(fileURLToPath(import.meta.url));
const rootDir = join(__dirname, "..");
const envPath = join(rootDir, ".env");
const outPath = join(rootDir, "backup.sql");

function loadEnv() {
  try {
    const content = readFileSync(envPath, "utf8");
    const env = {};
    for (const line of content.split("\n")) {
      const m = line.match(/^\s*([^#][^=]+)=(.*)$/);
      if (m) env[m[1].trim()] = m[2].trim().replace(/^["']|["']$/g, "");
    }
    return env;
  } catch (e) {
    return {};
  }
}

const env = loadEnv();
const databaseUrl = env.DATABASE_URL || process.env.DATABASE_URL;

if (!databaseUrl) {
  console.error("ERROR: DATABASE_URL is not set.");
  console.error("Add DATABASE_URL to your .env file. Get it from:");
  console.error("  Supabase Dashboard -> Project Settings -> Database -> Connection string (URI)");
  process.exit(1);
}

function escapeSql(val) {
  if (val === null) return "NULL";
  if (typeof val === "boolean") return val ? "true" : "false";
  if (typeof val === "number" && !Number.isNaN(val)) return String(val);
  if (Buffer.isBuffer(val)) return "'" + val.toString("hex").replace(/'/g, "''") + "'";
  return "'" + String(val).replace(/'/g, "''").replace(/\\/g, "\\\\") + "'";
}

async function main() {
  const client = new pg.Client({ connectionString: databaseUrl });
  const lines = [];
  lines.push("-- Backup generated by db-dump-node.js");
  lines.push("-- " + new Date().toISOString());
  lines.push("");

  try {
    await client.connect();
  } catch (e) {
    console.error("Failed to connect to database:", e.message);
    if (e.code === "ERR_INVALID_URL") {
      console.error("Fix DATABASE_URL in .env. Format: postgresql://postgres:YOUR_PASSWORD@db.PROJECT_REF.supabase.co:5432/postgres");
      console.error("  (There must be an @ between the password and the host.)");
    } else {
      console.error("Check DATABASE_URL in .env (correct host, password, port).");
    }
    process.exit(1);
  }

  try {
    const tablesRes = await client.query(`
      SELECT table_schema, table_name
      FROM information_schema.tables
      WHERE table_schema = 'public' AND table_type = 'BASE TABLE'
      ORDER BY table_schema, table_name
    `);

    for (const { table_schema, table_name } of tablesRes.rows) {
      const fullName = `"${table_schema}"."${table_name}"`;
      lines.push(`-- Table: ${fullName}`);
      const colsRes = await client.query(
        `SELECT column_name, data_type, is_nullable, column_default
         FROM information_schema.columns
         WHERE table_schema = $1 AND table_name = $2
         ORDER BY ordinal_position`,
        [table_schema, table_name]
      );
      const colNames = colsRes.rows.map((r) => r.column_name);
      const colList = colNames.map((c) => `"${c}"`).join(", ");
      const createParts = colsRes.rows.map((c) => {
        let def = `"${c.column_name}" ${c.data_type}`;
        if (c.column_default) def += " DEFAULT " + c.column_default;
        if (c.is_nullable === "NO") def += " NOT NULL";
        return def;
      });
      lines.push(`CREATE TABLE IF NOT EXISTS ${fullName} (`);
      lines.push(createParts.join(",\n"));
      lines.push(");");
      lines.push("");

      const res = await client.query(`SELECT * FROM ${fullName}`);
      for (const row of res.rows) {
        const vals = colNames.map((c) => escapeSql(row[c]));
        lines.push(`INSERT INTO ${fullName} (${colList}) VALUES (${vals.join(", ")});`);
      }
      if (res.rows.length > 0) lines.push("");
    }

    writeFileSync(outPath, lines.join("\n"), "utf8");
    console.log("Backup written to backup.sql");
  } finally {
    await client.end();
  }
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
